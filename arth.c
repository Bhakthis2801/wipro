//C program to demonstrate arithmetic operators

#include<stdio.h>

int main()
{
        int a=9,b=4,c;

        c=a+b;
        printf("Result = %d\n",c);

        c=a-b;
        printf("Result = %d\n",c);

        c=a*b;
        printf("Result = %d\n",c);

        c=a/b;
        printf("Result = %d\n",c);

        c=a%b;
        printf("Remainder = %d\n",c);

        return 0;
}
